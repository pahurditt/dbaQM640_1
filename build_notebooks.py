nb06 = [
    md(
        "# 06 — RQ4: Weighted-Scoring MCDA for Stage-1 Micro-Intervention "
        "Prioritization\n"
        "\n"
        "**Question:** Which Stage-1 micro-intervention should be implemented "
        "first to achieve the largest controlled flow improvement?\n"
        "\n"
        "**Method:** Weighted-scoring multi-criteria decision analysis (Saaty, "
        "2008) over four candidate Stage-1 micro-interventions. Sensitivity "
        "analysis with ±10% weight perturbation across all six pairwise "
        "weight combinations confirms rank stability."
    ),
    code(
        "from pathlib import Path\n"
        "import numpy as np\n"
        "import pandas as pd\n"
        "\n"
        "ROOT = Path('..').resolve()\n"
        "PROC = ROOT / 'data' / 'processed'"
    ),
    md(
        "## Candidate Stage-1 micro-interventions\n"
        "\n"
        "Each scored on three indicators (raw values are SME-elicited; "
        "replace with team estimates when running on real data):\n"
        "1. **expected uplift in first-time-right** (proportion)\n"
        "2. **expected reduction in SLA-breach rate** (proportion)\n"
        "3. **implementation feasibility** (0–10 scale)"
    ),
    code(
        "raw = pd.DataFrame({\n"
        "    'intervention': ['Auto-completeness gating',\n"
        "                     'Risk-score-based STP/escalation routing',\n"
        "                     'Product-aware validation checklists',\n"
        "                     'Channel-specific routing rules'],\n"
        "    'ftr_uplift':            [0.082, 0.064, 0.041, 0.031],\n"
        "    'sla_breach_reduction':  [0.058, 0.055, 0.030, 0.029],\n"
        "    'feasibility':           [7.5,   8.0,   7.0,   9.0],\n"
        "})\n"
        "raw"
    ),
    md("## Min-max normalize each indicator to [0, 1]"),
    code(
        "norm = raw.copy()\n"
        "for col in ['ftr_uplift', 'sla_breach_reduction', 'feasibility']:\n"
        "    lo, hi = raw[col].min(), raw[col].max()\n"
        "    norm[col + '_n'] = (raw[col] - lo) / (hi - lo)\n"
        "norm[['intervention','ftr_uplift_n','sla_breach_reduction_n','feasibility_n']]"
    ),
    md("## Composite priority score (base case: 0.40 / 0.35 / 0.25)"),
    code(
        "weights_base = {'ftr_uplift_n': 0.40, 'sla_breach_reduction_n': 0.35,\n"
        "                'feasibility_n': 0.25}\n"
        "norm['score'] = sum(norm[c] * w for c, w in weights_base.items())\n"
        "ranked = norm.sort_values('score', ascending=False)\n"
        "ranked[['intervention', 'score']].round(3).reset_index(drop=True)"
    ),
    md("## Sensitivity — ±10% perturbation across all weight pairs"),
    code(
        "import itertools\n"
        "perturbations = []\n"
        "weights = list(weights_base.items())\n"
        "for (a, wa), (b, wb) in itertools.combinations(weights, 2):\n"
        "    for delta in [+0.10, -0.10]:\n"
        "        new = dict(weights_base)\n"
        "        new[a] = wa * (1 + delta); new[b] = wb * (1 - delta)\n"
        "        s = sum(new.values())\n"
        "        for k in new: new[k] /= s\n"
        "        scores = sum(norm[c] * w for c, w in new.items())\n"
        "        top = norm.iloc[scores.idxmax()].intervention\n"
        "        perturbations.append({'shifted_pair': f'{a}↔{b}',\n"
        "            'delta': delta, 'top_intervention': top,\n"
        "            'top_score': float(scores.max())})\n"
        "pd.DataFrame(perturbations)"
    ),
    md(
        "## Hypothesis test\n"
        "H4₀ (equal priority across interventions) is rejected if the top score "
        "is materially higher than the second-place score AND the rank is "
        "stable under all ±10% perturbations."
    ),
    code(
        "scores = ranked.score.values\n"
        "spread = scores[0] - scores[1]\n"
        "top_stable = all(p['top_intervention'] == ranked.iloc[0].intervention\n"
        "                 for p in perturbations)\n"
        "print(f'Top score: {scores[0]:.3f}')\n"
        "print(f'Spread to 2nd place: {spread:.3f}')\n"
        "print(f'Top intervention stable under all perturbations: {top_stable}')\n"
        "print()\n"
        "print('H4_0 rejected:' if (spread > 0.05 and top_stable) else 'H4_0 not rejected:',\n"
        "      ranked.iloc[0].intervention,\n"
        "      'is the unambiguous first-tranche Stage-1 micro-intervention.')"
    ),
]
write_nb("06_mcda_rq4.ipynb", nb06)

