"""
build_notebooks.py — generate the six Jupyter notebooks for the capstone package.

Each notebook is created as a valid .ipynb (nbformat 4) with markdown narrative
cells alternating with code cells. Notebooks load the synthetic processed CSVs
under data/processed/ and produce figures into figures/.

When the author runs them with real BPIC-derived data (same schema), the
notebooks will produce real outputs without modification.
"""

import json
import os
from pathlib import Path

NB_DIR = Path(__file__).resolve().parent.parent / "notebooks"
NB_DIR.mkdir(parents=True, exist_ok=True)


def md(text: str) -> dict:
    return {
        "cell_type": "markdown",
        "metadata": {},
        "source": text.splitlines(keepends=True),
    }


def code(src: str) -> dict:
    return {
        "cell_type": "code",
        "metadata": {},
        "execution_count": None,
        "outputs": [],
        "source": src.splitlines(keepends=True),
    }


def write_nb(filename: str, cells: list) -> None:
    nb = {
        "cells": cells,
        "metadata": {
            "kernelspec": {
                "display_name": "Python 3",
                "language": "python",
                "name": "python3",
            },
            "language_info": {
                "name": "python",
                "version": "3.11",
                "mimetype": "text/x-python",
                "file_extension": ".py",
                "pygments_lexer": "ipython3",
                "codemirror_mode": {"name": "ipython", "version": 3},
                "nbconvert_exporter": "python",
            },
        },
        "nbformat": 4,
        "nbformat_minor": 5,
    }
    out = NB_DIR / filename
    out.write_text(json.dumps(nb, indent=1))
    print(f"  wrote {out.relative_to(NB_DIR.parent)}")


